package org.jandjzone.buildroid.objs;


/**
 * Base message class of websocket
 * @author Jason
 *
 */
public class WebsocketMessageBase {
	public static final String MESSAGE_TYPE_BUILD_START = "build_start";
	public static final String MESSAGE_TYPE_BUILD_END = "build_end";
	public static final String MESSAGE_TYPE_BUILD_OUTPUT = "build_output";
	
	private String project_id;
	private String msg_type;
	
	public String getMsg_type() {
		return msg_type;
	}
	public void setMsg_type(String msg_type) {
		this.msg_type = msg_type;
	}
	public String getProject_id() {
		return project_id;
	}
	public void setProject_id(String project_id) {
		this.project_id = project_id;
	}
}
