package org.jandjzone.buildroid.spring_mvc;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.ComponentScan;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.EnableWebMvc;
import org.springframework.web.servlet.config.annotation.ResourceHandlerRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurerAdapter;
import org.springframework.web.servlet.view.InternalResourceViewResolver;
import org.springframework.web.servlet.view.JstlView;

/**
 * Mvc configuration class.
 * @author Jason
 *
 */
@Configuration
@EnableWebMvc
@ComponentScan(basePackages = { "org.jandjzone.buildroid.spring_mvc.controller" })
public class BuildMvcConfig extends WebMvcConfigurerAdapter {
	@Bean
	public InternalResourceViewResolver resolver() {
	    InternalResourceViewResolver resolver = new InternalResourceViewResolver();
	    resolver.setViewClass(JstlView.class);
	    resolver.setPrefix("/WEB-INF/views/");
	    resolver.setSuffix(".jsp");
	    return resolver;
    }
	
	/**
	 * Map resources including images,css and javascript files.
	 */
	@Override
	public void addResourceHandlers(final ResourceHandlerRegistry registry) {
	    registry.addResourceHandler("/resources/**").addResourceLocations("/resources/");
	    registry.addResourceHandler("/build_history/**").addResourceLocations("/build_history/");
	}
}
